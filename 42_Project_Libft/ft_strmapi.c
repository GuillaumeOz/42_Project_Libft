#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	size_t	i;
	size_t	len_s;
	char	*str;

	str = NULL;
	i = 0;
	if (s)
	{
		len_s = ft_strlen(s);
		str = (char*)malloc(len_s + 1);
		if (str == NULL)
			return (str);
		while (s[i])
		{
			str[i] = f(i, s[i]);
			i++;
		}
		str[i] = '\0';
	}
	return (str);
}
