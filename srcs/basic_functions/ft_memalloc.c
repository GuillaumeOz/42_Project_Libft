#include "libft.h"

void	*ft_memalloc(size_t size)
{
	unsigned char	*ptr;

	ptr = NULL;
	if (size)
	{
		if (!(ptr = (unsigned char*)malloc(size)))
			return (NULL);
		ft_memset(ptr, 0, size);
	}
	return ((void*)ptr);
}
